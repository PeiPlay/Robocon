void USART2_Send(void)
{

	float tempFloat[3];                   //定义的临时变量
	uint8_t tempData[16];                    //定义的传输Buffer
	
//	tempFloat[0] = euler.angle.yaw;
//	tempFloat[1] = YawMAF.yaw;
//	tempFloat[2] = euler.angle.yaw;
	tempFloat[0] = -mmc5983.magDataX;
	tempFloat[1] = -mmc5983.magDataY;
	tempFloat[2] = -mmc5983.magDataZ;
//	tempFloat[0] = Comm_CAN.roll;

	memcpy(tempData, (uint8_t *)tempFloat, sizeof(tempFloat));//通过拷贝把数据重新整理
	
	tempData[12] = 0x00;                    //写入结尾数据
	tempData[13] = 0x00;
	tempData[14] = 0x80;
	tempData[15] = 0x7f;
 
  /*##-2- Start the transmission process #####################################*/  
  /* While the UART in reception process, user can transmit data through 
     "aTxBuffer" buffer */
  if (HAL_UART_Transmit_DMA(&huart2, (uint8_t *)tempData, 16) != HAL_OK)
  {
    Error_Handler();
  }
	 /*##-3- Wait for the end of the transfer ###################################*/   
  while (Usart2TxReady != SET)
  {
  }
  /* Reset transmission flag */
  Usart2TxReady = RESET;
}

